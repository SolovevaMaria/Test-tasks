-- ==============================================
-- ШАГ 1: Создание нормализованных таблиц
-- ==============================================

-- 1.1 Таблица стран
CREATE TABLE Countries (
    CountryID INT IDENTITY(1,1) PRIMARY KEY,
    CountryCode NVARCHAR(10) NOT NULL,
    CountryName NVARCHAR(100) NOT NULL,
    CONSTRAINT UQ_Country_Code UNIQUE (CountryCode)
);
GO

-- 1.2 Таблица станций
CREATE TABLE Stations (
    StationID INT IDENTITY(1,1) PRIMARY KEY,
    StationCode NVARCHAR(20) NOT NULL,
    StationName NVARCHAR(200) NOT NULL,
    CountryID INT NOT NULL,
    CONSTRAINT UQ_Station_Code UNIQUE (StationCode)
);
GO

-- 1.3 Таблица контрагентов (отправители и получатели)
CREATE TABLE CargoOwners (
    CargoOwnerID INT IDENTITY(1,1) PRIMARY KEY,
    CargoOwnerName NVARCHAR(250) NOT NULL,
    CargoOwnerAddress NVARCHAR(500) NOT NULL,
    CONSTRAINT UQ_CargoOwner UNIQUE (CargoOwnerName, CargoOwnerAddress)
);
GO

-- 1.4 Таблица грузов
CREATE TABLE Cargo (
    CargoID INT IDENTITY(1,1) PRIMARY KEY,
    CargoCode NVARCHAR(50) NOT NULL,
    CargoName NVARCHAR(500) NOT NULL,
    CONSTRAINT UQ_Cargo_Code UNIQUE (CargoCode)
);
GO

-- 1.5 Главная таблица накладных
CREATE TABLE Waybills (
    WaybillID INT IDENTITY(1,1) PRIMARY KEY,
    SourceID INT NULL,                    -- Оригинальный ID из SourceWaybills
    ExternalNumber NVARCHAR(50) NOT NULL, -- Внешний номер
    ShipmentType NVARCHAR(10) NULL,       -- Вид отправки
    InvoiceNumber NVARCHAR(50) NULL,      -- Номер фактуры
    WagonNumber NVARCHAR(20) NOT NULL,    -- Номер вагона
    WagonType NVARCHAR(50) NULL,          -- Род вагона
    WagonCapacity DECIMAL(10,2) NULL,     -- Грузоподъемность
    WagonAxles INT NULL,                   -- Количество осей
    DepartureCountryID INT NOT NULL,       -- Страна отправления (ID)
    DepartureStationID INT NOT NULL,       -- Станция отправления (ID)
    ShipperID INT NOT NULL,                 -- Грузоотправитель (ID)
    DestinationStationID INT NOT NULL,      -- Станция назначения (ID)
    ConsigneeID INT NOT NULL,               -- Грузополучатель (ID)
    CargoID INT NOT NULL,                    -- Груз (ID)
    NumberOfPackages INT NULL,               -- Количество мест
    PackageType NVARCHAR(100) NULL,          -- Вид упаковки
    CargoDescription NVARCHAR(MAX) NULL,     -- Описание груза
    GrossWeight DECIMAL(10,2) NULL,          -- Вес брутто
    NetWeight DECIMAL(10,2) NULL,            -- Вес груза
    TareWeight DECIMAL(10,2) NULL,           -- Вес тары
    SpecialMarks NVARCHAR(MAX) NULL,         -- Особые отметки
    DeletedFlag BIT NULL,                    -- Признак удаления (0/1)
    ResponsiblePerson NVARCHAR(200) NULL     -- Ответственное лицо
);
GO

